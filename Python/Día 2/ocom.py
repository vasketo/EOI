#Ejercicio con operadores de comparación

a = 20
b = 10

print ( "a =", a )
print ( "b =", b )

# Comparo si a y b son iguales
if  a == b :
    print( "a y b son iguales" )


# Comparo si a y b son distintos.
if  a != b :
    print( "a y b son distintos" )


# Comparo si a < b
if  a < b :
    print( "a es menor que b" )


# Comparo si a > b
if  a > b :
    print( "a es mayor que b" )


# Comparo si a <= b
if  a <= b :
    print( "a es menor o igual que b" )


# Comparo si a >= b
if  a >= b :
    print( "a es mayor o igual que b" )


